function entry(value: LibraryEntryLocalization): LibraryEntryLocalization {
  return Object.freeze(value);
}

function step(
  title: string,
  instruction: string,
  check: string,
  artifactExamples: readonly (string | { readonly caption: string; readonly code: string })[],
  featureLinkLabel?: string,
) {
  return Object.freeze({
    title,
    instruction,
    check,
    artifactExamples: Object.freeze(
      artifactExamples.map((value) =>
        typeof value === "string" ? Object.freeze({ caption: value }) : Object.freeze(value),
      ),
    ),
    ...(featureLinkLabel === undefined ? {} : { featureLinkLabel }),
  });
}
