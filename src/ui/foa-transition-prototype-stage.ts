function parseIntegerInput(value: string): number {
  const normalized = value.trim();
  if (!/^-?\d+$/u.test(normalized)) throw new TypeError("Expected an integer input");
  const parsed = Number(normalized);
  if (!Number.isSafeInteger(parsed))
    throw new RangeError("Integer input is outside the safe range");
  return parsed;
}

