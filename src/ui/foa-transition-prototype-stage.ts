function subscribeToMediaQuery(
  query: MediaQueryList | null,
  listener: (matches: boolean) => void,
): () => void {
  if (query === null) return () => {};
  const onChange = (event: MediaQueryListEvent): void => listener(event.matches);
  query.addEventListener?.("change", onChange);
  return () => query.removeEventListener?.("change", onChange);
}

function createResizeObserver(
  ownerDocument: Document,
  callback: () => void,
): ResizeObserver | null {
  const Constructor = ownerDocument.defaultView?.ResizeObserver;
  return Constructor === undefined ? null : new Constructor(callback);
}

function requestFrame(ownerDocument: Document, callback: FrameRequestCallback): number {
  return (
    ownerDocument.defaultView?.requestAnimationFrame?.(callback) ??
    ownerDocument.defaultView?.setTimeout(() => callback(performance.now()), 16) ??
    -1
  );
}

function cancelFrame(ownerDocument: Document, request: number): void {
  if (request < 0) return;
  if (ownerDocument.defaultView?.cancelAnimationFrame !== undefined) {
    ownerDocument.defaultView.cancelAnimationFrame(request);
  } else {
    ownerDocument.defaultView?.clearTimeout(request);
  }
}
