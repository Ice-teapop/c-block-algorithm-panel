function connectionGeometry(
  canvas: HTMLElement,
  from: HTMLElement,
  to: HTMLElement,
  constrainToVisibleCanvas = false,
): EdgeGeometry | null {
  const canvasRect = canvas.getBoundingClientRect();
  const fromRect = from.getBoundingClientRect();
  const toRect = to.getBoundingClientRect();
  if (canvasRect.width <= 0 || canvasRect.height <= 0 || fromRect.width <= 0 || toRect.width <= 0) {
    return null;
  }
  if (
    constrainToVisibleCanvas &&
    (!isFullyVisibleConnectionEndpoint(canvas, canvasRect, from, fromRect) ||
      !isFullyVisibleConnectionEndpoint(canvas, canvasRect, to, toRect))
  ) {
    return null;
  }
  const fromCenter = {
    x: fromRect.left - canvasRect.left + fromRect.width / 2,
    y: fromRect.top - canvasRect.top + fromRect.height / 2,
  };
  const toCenter = {
    x: toRect.left - canvasRect.left + toRect.width / 2,
    y: toRect.top - canvasRect.top + toRect.height / 2,
  };
  const rawStart = rectangleBoundaryPoint(fromCenter, toCenter, fromRect.width, fromRect.height, 0);
  const rawEnd = rectangleBoundaryPoint(toCenter, fromCenter, toRect.width, toRect.height, 0);
  const availableGap = Math.hypot(rawEnd.x - rawStart.x, rawEnd.y - rawStart.y);
  // A fixed clearance can cross over when adjacent cells are only a few pixels apart, reversing
  // the apparent arrow direction. Scale both clearances to leave at least one forward pixel.
  const clearanceScale = Math.min(1, Math.max(0, availableGap - 1) / 7);
  const start = rectangleBoundaryPoint(
    fromCenter,
    toCenter,
    fromRect.width,
    fromRect.height,
    2 * clearanceScale,
  );
  const end = rectangleBoundaryPoint(
    toCenter,
    fromCenter,
    toRect.width,
    toRect.height,
    5 * clearanceScale,
  );
  const controlX = (start.x + end.x) / 2;
  const rawControlY = connectionControlY(start.x, start.y, end.x, end.y);
  const controlY = constrainToVisibleCanvas
    ? Math.min(Math.max(rawControlY, 8), Math.max(8, canvasRect.height - 8))
    : rawControlY;
  return Object.freeze({
    startX: start.x,
    startY: start.y,
    endX: end.x,
    endY: end.y,
    controlX,
    controlY,
    length: quadraticBezierLength(start.x, start.y, controlX, controlY, end.x, end.y),
  });
}

function isFullyVisibleConnectionEndpoint(
  canvas: HTMLElement,
  canvasRect: DOMRect,
  endpoint: HTMLElement,
  endpointRect: DOMRect,
): boolean {
  if (!rectContains(canvasRect, endpointRect, 1)) return false;
  const getStyle = canvas.ownerDocument.defaultView?.getComputedStyle.bind(
    canvas.ownerDocument.defaultView,
  );
  for (
    let ancestor = endpoint.parentElement;
    ancestor !== null;
    ancestor = ancestor.parentElement
  ) {
    if (getStyle === undefined) continue;
    const style = getStyle(ancestor);
    const clipsX = /^(?:auto|clip|hidden|scroll)$/u.test(style.overflowX);
    const clipsY = /^(?:auto|clip|hidden|scroll)$/u.test(style.overflowY);
    if (!clipsX && !clipsY) continue;
    const ancestorRect = ancestor.getBoundingClientRect();
    if (
      (clipsX &&
        (endpointRect.left < ancestorRect.left - 1 ||
          endpointRect.right > ancestorRect.right + 1)) ||
      (clipsY &&
        (endpointRect.top < ancestorRect.top - 1 || endpointRect.bottom > ancestorRect.bottom + 1))
    ) {
      return false;
    }
  }
  const viewport = canvas.ownerDocument.defaultView;
  if (
    viewport !== null &&
    (endpointRect.left < -1 ||
      endpointRect.right > viewport.innerWidth + 1 ||
      endpointRect.top < -1 ||
      endpointRect.bottom > viewport.innerHeight + 1)
  ) {
    return false;
  }
  return true;
}

function rectContains(outer: DOMRect, inner: DOMRect, tolerance: number): boolean {
  return (
    inner.left >= outer.left - tolerance &&
    inner.right <= outer.right + tolerance &&
    inner.top >= outer.top - tolerance &&
    inner.bottom <= outer.bottom + tolerance
  );
}

function connectionControlY(startX: number, startY: number, endX: number, endY: number): number {
  const horizontal = Math.abs(endX - startX);
  const vertical = Math.abs(endY - startY);
  if (horizontal <= vertical) return (startY + endY) / 2;
  const arc = horizontal < 80 ? horizontal / 5 : Math.min(64, Math.max(32, horizontal / 4));
  return Math.min(startY, endY) - arc;
}

function bezierTokenKeyframes(geometry: EdgeGeometry): Keyframe[] {
  return Array.from({ length: 9 }, (_, index) => {
    const progress = index / 8;
    const inverse = 1 - progress;
    const x =
      inverse * inverse * geometry.startX +
      2 * inverse * progress * geometry.controlX +
      progress * progress * geometry.endX;
    const y =
      inverse * inverse * geometry.startY +
      2 * inverse * progress * geometry.controlY +
      progress * progress * geometry.endY;
    return {
      transform: tokenTransform(x, y),
      opacity: 0.45 + progress * 0.55,
      offset: progress,
    };
  });
}

function quadraticBezierLength(
  startX: number,
  startY: number,
  controlX: number,
  controlY: number,
  endX: number,
  endY: number,
): number {
  let length = 0;
  let previousX = startX;
  let previousY = startY;
  for (let index = 1; index <= 12; index += 1) {
    const progress = index / 12;
    const inverse = 1 - progress;
    const x =
      inverse * inverse * startX + 2 * inverse * progress * controlX + progress * progress * endX;
    const y =
      inverse * inverse * startY + 2 * inverse * progress * controlY + progress * progress * endY;
    length += Math.hypot(x - previousX, y - previousY);
    previousX = x;
    previousY = y;
  }
  return length;
}

function resolveFrameSourceSelection(
  source: string,
  model: PrototypeModel,
  frame: PrototypeFrame,
  frameIndex: number,
): ResolvedFrameSource {
  const anchors = "sourceAnchors" in model ? model.sourceAnchors : model.anchors;
  const anchor = anchors.find(({ id }) => id === frame.sourceAnchorId);
  if (anchor === undefined) {
    throw new RangeError(`Missing source anchor ${frame.sourceAnchorId}`);
  }
  const offset = source.indexOf(anchor.exact);
  if (offset < 0 || source.indexOf(anchor.exact, offset + 1) >= 0) {
    throw new RangeError(`Source anchor ${frame.sourceAnchorId} must occur exactly once`);
  }
  return Object.freeze({
    frameId: frame.id,
    frameIndex,
    sourceAnchorId: frame.sourceAnchorId,
    sourceLine: source.slice(0, offset).split(/\r?\n/u).length,
    sourceText: anchor.exact,
  });
}

function rectangleBoundaryPoint(
  center: { readonly x: number; readonly y: number },
  toward: { readonly x: number; readonly y: number },
  width: number,
  height: number,
  spacing: number,
): { readonly x: number; readonly y: number } {
  const deltaX = toward.x - center.x;
  const deltaY = toward.y - center.y;
  if (Math.abs(deltaX) < 0.001 && Math.abs(deltaY) < 0.001) return center;
  const halfWidth = Math.max(1, width / 2 + spacing);
  const halfHeight = Math.max(1, height / 2 + spacing);
  const scale = 1 / Math.max(Math.abs(deltaX) / halfWidth, Math.abs(deltaY) / halfHeight);
  return Object.freeze({ x: center.x + deltaX * scale, y: center.y + deltaY * scale });
}

function rectanglesOverlap(left: DOMRect, right: DOMRect): boolean {
  const horizontal = Math.min(left.right, right.right) - Math.max(left.left, right.left);
  const vertical = Math.min(left.bottom, right.bottom) - Math.max(left.top, right.top);
  return horizontal > 1 && vertical > 1;
}

function isVisibleInsideLocalScroller(node: HTMLElement, rectangle: DOMRect): boolean {
  const localScroller =
    typeof node.closest === "function"
      ? node.closest<HTMLElement>(".foa-transition-prototype__array")
      : null;
  if (
    localScroller === null ||
    (localScroller.scrollWidth <= localScroller.clientWidth + 1 &&
      localScroller.scrollHeight <= localScroller.clientHeight + 1)
  ) {
    return true;
  }
  const viewport = localScroller.getBoundingClientRect();
  const horizontal =
    Math.min(rectangle.right, viewport.right) - Math.max(rectangle.left, viewport.left);
  const vertical =
    Math.min(rectangle.bottom, viewport.bottom) - Math.max(rectangle.top, viewport.top);
  return horizontal > 1 && vertical > 1;
}

function keepChildVisible(container: HTMLElement, child: HTMLElement): void {
  const containerRect = container.getBoundingClientRect();
  const childRect = child.getBoundingClientRect();
  if (containerRect.width <= 0 || childRect.width <= 0) return;
  if (childRect.left < containerRect.left) {
    container.scrollLeft -= containerRect.left - childRect.left;
  } else if (childRect.right > containerRect.right) {
    container.scrollLeft += childRect.right - containerRect.right;
  }
}

function tokenTransform(x: number, y: number): string {
  return `translate3d(calc(${String(x)}px - 50%), calc(${String(y)}px - 50%), 0)`;
}

function playbackDelay(frame: PrototypeFrame): number {
  const kind = motionKind(frame);
  if (kind === "compare") return 1_650;
  if (kind === "shift") return 1_550;
  if (kind === "insert") return 1_450;
  return 1_300;
}

