function frameRule(model: PrototypeModel, frame: PrototypeFrame, locale: FoaLocale): string {
  if ("frames" in model) {
    const item = frame as FoaTransitionRuntimeFrame;
    if (model.lessonOrder === 63) {
      const input = item.state.inputValue ?? "?";
      const value = item.state.counterValue ?? "?";
      if (item.phase === "create") return "counter record created";
      if (item.phase === "initialise") return `counter.value ← input (${String(input)})`;
      if (item.phase === "bind") return "link → counter";
      if (item.phase === "resolve") return "link->value ↦ counter.value";
      if (item.phase === "write") return `counter.value ← ${String(value)}`;
      return `stdout ← ${String(value)}`;
    }
    const low = String(item.state.low ?? "?");
    const mid = String(item.state.mid ?? "?");
    const high = String(item.state.high ?? "?");
    if (item.phase === "initialise") return `range ← [${low}, ${high})`;
    if (item.phase === "calculate") return `mid ← ${low} + floor((${high} - ${low}) / 2)`;
    if (item.phase === "compare") return `values[${mid}] < target`;
    if (item.phase === "branch") {
      return item.branchOutcome === true ? `low ← ${mid} + 1` : `high ← ${mid}`;
    }
    return `lower_bound ← ${low}`;
  }

  const event = frame as FoaTransitionRuntime7580Event;
  if (model.lessonOrder === 75) {
    const active = event.stackFrames.find(({ frameId }) => frameId === event.activeFrameId);
    if (event.kind === "call-enter") return `push moves(${String(active?.argument ?? "?")})`;
    if (event.kind === "call-exit") {
      return `pop moves(${String(active?.argument ?? "?")}) → ${String(active?.returnValue ?? "?")}`;
    }
    return `stdout ← ${event.output ?? "?"}`;
  }
  const dependency = event.dependencies[0];
  if (event.kind === "dependency-read" && dependency !== undefined) {
    return `paths[${String(dependency.to.row)}][${String(dependency.to.column)}] += paths[${String(dependency.from.row)}][${String(dependency.from.column)}]`;
  }
  if (event.kind === "array-write" && event.activeCell !== null) {
    const value = event.matrix[event.activeCell.row]?.[event.activeCell.column] ?? "?";
    return `paths[${String(event.activeCell.row)}][${String(event.activeCell.column)}] ← ${String(value)}`;
  }
  if (event.kind === "cell-skip" && event.activeCell !== null) {
    return locale === "zh"
      ? `跳过障碍 (${String(event.activeCell.row)}, ${String(event.activeCell.column)})`
      : `skip obstacle (${String(event.activeCell.row)}, ${String(event.activeCell.column)})`;
  }
  return `stdout ← ${event.output ?? "?"}`;
}

function motionKind(frame: PrototypeFrame): "pick-key" | "compare" | "shift" | "insert" | "settle" {
  if ("phase" in frame) {
    if (frame.phase === "compare") return "compare";
    if (frame.phase === "write") return "insert";
    if (frame.phase === "branch") return "shift";
    return "settle";
  }
  if (frame.kind === "dependency-read") return "compare";
  if (frame.kind === "array-write") return "insert";
  if (frame.kind === "call-enter" || frame.kind === "call-exit") return "shift";
  return "settle";
}

